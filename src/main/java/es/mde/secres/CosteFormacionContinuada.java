package es.mde.secres;

public class CosteFormacionContinuada {

  private String clave;
  private int valor;

  public CosteFormacionContinuada() {
    super();
  }

  public String getClave() {
    return clave;
  }

  public void setClave(String clave) {
    this.clave = clave;
  }

  public int getValor() {
    return valor;
  }

  public void setValor(int valor) {
    this.valor = valor;
  }

}